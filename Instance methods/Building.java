class Building
{
	public void noOfFloors(int number)
	{
		System.out.println("Number of floors in Building:"+number);
	}
	public void buildingLocation(String location)
	{
		System.out.println("Building location:"+location);
	}
	public void buildingName(String name)
	{
		System.out.println("Name of Building:"+name);
	}
	public void price(int amount)
	{
		System.out.println("price of an building:"+amount);
	}
	public void ownerName(String name)
	{
		System.out.println("Owner of the building:"+name);
	}
}