class Beds
{
	public static String[] bedTypes(String[] types)
    {
        System.out.println("Types of Beds");
        return types;
    }

    public static int[] sizes(int[] size)
    {
        System.out.println("Bed Sizes");
        return size;
    }

    public static long[] serialNumbers(long[] serials)
    {
        System.out.println("Serial Numbers of Beds");
        return serials;
    }
	public static double[] prices(double[] price)
    {
        System.out.println("Bed Prices");
        return price;
    }

    public static float[] bedWeights(float[] weights)
    {
        System.out.println("Weights of Beds ");
        return weights;
    }

    public static char[] qualityGrades(char[] grades)
    {
        System.out.println("Quality Grades of Beds");
        return grades;
    }

    public static boolean[] isWooden(boolean[] wooden)
    {
        System.out.println("Wooden or Not");
        return wooden;
    }
}